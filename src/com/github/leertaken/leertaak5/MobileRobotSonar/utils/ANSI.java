package com.github.leertaken.leertaak5.MobileRobotSonar.utils;

/**
 * Class representing the ANSI codec simplified.
 *
 * @author Nils Berlijn
 * @author Tom Broenink
 * @version 1.0
 */
public final class ANSI {

    /**
     * The ANSI color black.
     */
    public static final String ANSI_BLACK = "\u001B[30m";

    /**
     * The ANSI color red.
     */
    public static final String ANSI_RED = "\u001B[31m";

    /**
     * The ANSI color green.
     */
    public static final String ANSI_GREEN = "\u001B[32m";

    /**
     * The ANSI color yellow.
     */
    public static final String ANSI_YELLOW = "\u001B[33m";

    /**
     * The ANSI color blue.
     */
    public static final String ANSI_BLUE = "\u001B[34m";

    /**
     * The ANSI color magenta.
     */
    public static final String ANSI_MAGENTA = "\u001B[35m";

    /**
     * The ANSI color cyan.
     */
    public static final String ANSI_CYAN = "\u001B[36m";

    /**
     * The ANSI color white.
     */
    public static final String ANSI_WHITE = "\u001B[37m";

}
